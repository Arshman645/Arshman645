var number1 = prompt("Enter your first number");
var number2 = prompt("Enter your second number");
var operater = prompt("Enter your operater");

if(operater == "+"){
    var result = number1 + number2
    alert(result)
}

if(operater == "-"){
    var result= number1 - number2
    alert(result)
}

if(operater == "*"){
    var result= number1 * number2
    alert(result)
}

if(operater == "/"){
    var result= number1 / number2
    alert(result)
}